public class Discipline {
  String id;
  String title;
  String program;
  String schedule;
  String commentary;

  public  Discipline(){}

  public Discipline(String id,String title,String program, String schedule, String commentary ){
    this.id = id;
    this.title = title;
    this.program = program;
    this.schedule = schedule;
    this.commentary = commentary;
  }
}
